# --- Feature Engineering from df_eventos for df_carga ---
# Aggregate event features within a 24-hour window before each df_carga timestamp

def aggregate_event_features(df_carga_row, df_eventos):
    locacion = df_carga_row['LOCACION']
    timestamp = df_carga_row['TIMESTAMP']

    # Define the 24-hour window before the current df_carga timestamp
    window_start = timestamp - pd.Timedelta(hours=24)

    # Filter events for the specific location and within the 24-hour window
    relevant_events = df_eventos[(
        (df_eventos['LOCACION'] == locacion) &
        (df_eventos['TIMESTAMP'] < timestamp) &
        (df_eventos['TIMESTAMP'] >= window_start)
    )]

    # Initialize features
    features = {
        'EVENT_COUNT_24H': 0,
        'MEAN_CURR_24H': 0.0,
        'MAX_CURR_24H': 0.0,
        'STD_CURR_24H': 0.0,
        'GRP_1_COUNT_24H': 0,
        'GRP_2_COUNT_24H': 0,
        'SHOT_0_COUNT_24H': 0,
        'SHOT_1_COUNT_24H': 0,
        'SHOT_2_COUNT_24H': 0,
        'SHOT_3_COUNT_24H': 0
    }

    if not relevant_events.empty:
        features['EVENT_COUNT_24H'] = len(relevant_events)
        if 'CURR' in relevant_events.columns and relevant_events['CURR'].notna().any():
            features['MEAN_CURR_24H'] = relevant_events['CURR'].mean()
            features['MAX_CURR_24H'] = relevant_events['CURR'].max()
            features['STD_CURR_24H'] = relevant_events['CURR'].std()

        features['GRP_1_COUNT_24H'] = (relevant_events['GRP'] == 1).sum()
        features['GRP_2_COUNT_24H'] = (relevant_events['GRP'] == 2).sum()
        features['SHOT_0_COUNT_24H'] = (relevant_events['SHOT'] == 0).sum()
        features['SHOT_1_COUNT_24H'] = (relevant_events['SHOT'] == 1).sum()
        features['SHOT_2_COUNT_24H'] = (relevant_events['SHOT'] == 2).sum()
        features['SHOT_3_COUNT_24H'] = (relevant_events['SHOT'] == 3).sum()

    return pd.Series(features)

