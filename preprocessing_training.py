# --- PASO 1: Clasificación de Severidad en Eventos (re-run to ensure df_eventos is prepared) ---
def clasificar_severidad(event_std):
    rojo = ['TRIP', 'BC_T', 'BG_T', 'ABC', 'AB_T', 'AG_T', 'CG_T', 'AC_T']
    amarillo = ['AB', 'CA', 'BC', 'AG', 'BG', 'CG', 'AC']
    if event_std in rojo: return 2
    if event_std in amarillo: return 1
    return 0

