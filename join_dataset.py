def extraer_locacion_mejorada(linea, nombre_archivo):
    # Prioridad absoluta al diccionario manual
    if nombre_archivo in CORRECCIONES_MANUALES:
        return CORRECCIONES_MANUALES[nombre_archivo]

    # Si no está en el diccionario, intentamos extraer por patrón (fallback)
    linea = linea.upper()
    match = re.search(r'(?:REDUCTOR|RECLOSER)\s+(?:[A-Z]\d+\s+)?([A-ZÁÉÍÓÚÑ]+)', linea)
    return match.group(1).strip() if match else "DESCONOCIDO"

def extractor_union_datos(base_path, bloques):
    all_ldp = []
    all_hist = []

    for bloque in bloques:
        folder = os.path.join(base_path, bloque)
        if not os.path.exists(folder):
            print(f"Advertencia: No se encontró la carpeta {folder}")
            continue

        for archivo in os.listdir(folder):
            # --- FILTRO DE SEGURIDAD ---
            # Solo procesa si el archivo está en nuestras correcciones manuales
            if archivo not in CORRECCIONES_MANUALES:
                continue

            path = os.path.join(folder, archivo)
            lineas = []
            try:
                if archivo.endswith('.xlsx'):
                    df_raw = pd.read_excel(path, header=None)
                    lineas = df_raw[0].astype(str).tolist()
                else:
                    with open(path, 'r', encoding='latin-1') as f:
                        lineas = f.readlines()
            except Exception as e:
                print(f"Error leyendo {archivo}: {e}")
                continue

            seccion_actual = None
            locacion_actual = extraer_locacion_mejorada("", archivo)

            for line in lineas:
                clean_line = line.strip()
                if not clean_line or "#NAME?" in clean_line:
                    continue

                # Identificar secciones
                if any(x in clean_line for x in ["=>HIST", "DATE TIME EVENT"]):
                    seccion_actual = "HIST"
                    continue
                elif any(x in clean_line for x in ["=>LDP", "DATE TIME IA IB IC"]):
                    seccion_actual = "LDP"
                    continue

                partes = clean_line.split()
                if not partes or not partes[0].isdigit():
                    continue

                # --- PROCESAMIENTO HIST (EVENTOS) ---
                if seccion_actual == "HIST" and len(partes) >= 9:
                    idx_locat = next((i for i, p in enumerate(partes) if "$" in p), None)
                    if idx_locat is None: continue

                    event = " ".join(partes[3:idx_locat])
                    curr = partes[idx_locat + 1] if len(partes) > idx_locat + 1 else None
                    freq = partes[idx_locat + 2] if len(partes) > idx_locat + 2 else None
                    grp  = partes[idx_locat + 3] if len(partes) > idx_locat + 3 else None
                    shot = partes[idx_locat + 4] if len(partes) > idx_locat + 4 else "0"
                    targets = "".join(partes[idx_locat + 5:])

                    all_hist.append({
                        '#': partes[0], 'DATE': partes[1], 'TIME': partes[2],
                        'EVENT': event, 'LOCAT': partes[idx_locat], 'CURR': curr,
                        'FREQ': freq, 'GRP': grp, 'SHOT': str(shot).zfill(8),
                        'TARGETS': targets, 'LOCACION': locacion_actual, 'ARCHIVO': archivo
                    })

                # --- PROCESAMIENTO LDP (CARGA) ---
                elif seccion_actual == "LDP" and len(partes) >= 11:
                    d = {
                        '#': partes[0], 'DATE': partes[1], 'TIME': partes[2],
                        'IA': partes[3], 'IB': partes[4], 'IC': partes[5], 'IG': partes[6],
                        'VAB': partes[7], 'VBC': partes[8], 'VCA': partes[9], 'FREQ': partes[10],
                        'LOCACION': locacion_actual, 'ARCHIVO': archivo
                    }
                    if len(partes) >= 12: d['MW3'] = partes[11]
                    if len(partes) >= 13: d['MVAR3'] = partes[12]
                    if len(partes) >= 14: d['PF3'] = partes[13]
                    if len(partes) >= 15: d['3I2'] = partes[14]
                    all_ldp.append(d)

    df_l = pd.DataFrame(all_ldp) if all_ldp else pd.DataFrame()
    df_h = pd.DataFrame(all_hist) if all_hist else pd.DataFrame()

    return df_l, df_h

