def normalizar_locaciones(df):
    if df is None or 'LOCACION' not in df.columns: return df

    # First, normalize unicode characters and remove extra spaces
    df['LOCACION'] = df['LOCACION'].astype(str).str.normalize('NFKD').str.encode('ascii', errors='ignore').str.decode('utf-8').str.strip().replace(r'\s+', ' ', regex=True)

    # Then, apply the specific mappings
    df['LOCACION'] = df['LOCACION'].replace(MAPEO_ESTANDAR)

    return df

